def in_odd(num):
    if num % 2 == 0:
        print("짝수 입니다!")
        return False
    else:
        print("홀수 입니다!")
        return True

def is_seven(num):
    if num % 7 == 0:
        print("7의 배수입니다!")
        return True
    else:
        print("7의 배수가 아닙니다!")
        return False
